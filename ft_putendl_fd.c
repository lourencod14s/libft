#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	ft_putstr_fd(s, fd);
	ft_putchar_fd('\n', fd);
}

/*
** The ft_putendl_fd() function output a string to a file descriptor, 
** followed by a new line.
*/